Atalho para o Terminal Puro (Linux) = Ctrl + ALt + F1
Atalho para voltar = Ctrl + ALt + F7

~ = Diretório pessoal 
$ = Usuário comum
LS = Comando de "Listar"
 * ls -lh = Listar arquivos de diretórios (detalhado)
Ctrl + L = Limpar
cd = andar entre diretórios
cd / = diretório raiz
cd - = volta para onde estava

